#Local variables Names
#starts with lower case
animal = cat

#Local variable scope
#once assigned, all uses of the name in the scope are considered local variables

#Instance variables
#Shared across all methods for the same object
#starts with an @
@instance_variable = value

#Class variables
#shared between a class, subclasses and instances
#starts with an @@
@@class_variable  = 0

#Global variables
#Accessible everywhere
#start with a $
$global = 0
